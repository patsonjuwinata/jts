package jts.model;

public class Parkir {
	
	private String noPlatKendaraan;
	private String lokasiParkir;
	  
	public String getNoPlatKendaraan() {
		return this.noPlatKendaraan;
	}
	  
	public void setNoPlatKendaraan(String noPlatKendaraan) {
		this.noPlatKendaraan = noPlatKendaraan;
	}

	public String getLokasiParkir() {
		return this.lokasiParkir;
	}
	  
	public void setLokasiParkir(String lokasiParkir) {
		this.lokasiParkir = lokasiParkir;
	}

}

